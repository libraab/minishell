#include "../../includes/minishell.h"

void	ft_env(void)
{
	int	i;

	i = -1;
	while (g_exe.env[++i])
	{
		if (g_exe.env[i][0] == '?')
			continue ;
		printf("%s\n", g_exe.env[i]);
	}
}
