#include "../../includes/minishell.h"

void	ft_env(void)
{
	int	i;

	i = -1;
	while (exe.env[++i])
	{
		if (exe.env[i][0] == '?')
			continue ;
		printf("%s\n", exe.env[i]);
	}
}
