#include "libft.h"

size_t	ft_intlen(int n)
{
	size_t		result;
	long int	c;

	c = n;
	result = 1;
	if (c < 0)
		c = -c;
	while (c / 10 > 0)
	{
		result++;
		c /= 10;
	}
	return (result);
}
