#include "libft.h"

int	ft_lnbrcmp(char *s1, char *s2)
{
	long	n1;
	long	n2;

	n1 = ft_atol(s1);
	n2 = ft_atol(s2);
	if (n1 < n2)
		return (-1);
	else if (n1 > n2)
		return (1);
	return (0);
}
